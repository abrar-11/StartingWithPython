# ===String Data type===

# Literal Assignment
firstName = "Bruce"
lastName = "Wayne"

print(type(firstName))
print(type(firstName) == str)
print(isinstance(firstName, str))


# constructor function
pizza = str("Pepperoni")
print(type(pizza))
print(type(pizza) == str)
print(isinstance(pizza, str))


